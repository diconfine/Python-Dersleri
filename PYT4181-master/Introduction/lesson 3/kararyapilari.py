# == (eşit eşittir) => Soldaki değerin sağdaki değere eşit olma durumu.
    # 1 == 2 => true  (if)
    # 1 == 2 => false (else)

#----------------------------------------------------------------------------------------

# != (eşit değilse) => soldaki değerin sağdakine eşit olmama durumu
    # 1 != 2 => true  (if)
    # 1 != 1 => false (else)

# -------------------------------------------------------------------------------------------------------

# > (Büyüktür) => soldaki değerin sağdakinden büyük olma durumu.
#     2 > 1 => true (if)
#     1 > 2 = false (else)

# ------------------------------------------------------------------------------------

# < (küçüktür ) soldaki değerin sağdakinden küçük olma durumu.

#   1 < 2 => true (if)
#   2 < 1 => false (else)

#---------------------------------------------------------------------------------------

# >= (Büyük eşittir) soldaki değerin sağdakinden büyük veya eşit olma durumu.

# 1 >= 1 => true (if) (eşit olma durumu)
# 2 >= 1 => true (if) (Büyük olma durumu)
# 1 >= 2 => false (else)


#-------------------------------------------------------------------------------------------

# <= (küçük eşittir) soldaki değerin sağdakine eşit veya küçük olma durumu.

# 1 <= 1 => true (if) (eşit olma durumu)
# 1 <= 2 => true (if) (küçük olma durumu)
# 3 <= 1 => false (else)
